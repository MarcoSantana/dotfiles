#!/usr/bin/env bash
#
# theme-switch.sh — Switch between color schemes system-wide
# Uses gum for interactive selection when run without args.
#
# Usage:
#   theme-switch.sh                        # gum TUI
#   theme-switch.sh catppuccin mocha
#   theme-switch.sh catppuccin macchiato
#   theme-switch.sh gruvbox dark
#   theme-switch.sh gruvbox light
#   theme-switch.sh solarized dark
#   theme-switch.sh solarized light
#   theme-switch.sh next                   # cycle through all themes
#
set -euo pipefail

DOTFILES="${DOTFILES:-$HOME/dotfiles}"
THEMES_DIR="$DOTFILES/themes"
DRY_RUN=false

# ── Ensure gum ────────────────────────────────────────────────────
if ! command -v gum &>/dev/null; then
  echo "Installing gum..."
  curl -fsSL https://github.com/charmbracelet/gum/releases/latest/download/gum_amd64.deb \
    -o /tmp/gum.deb && sudo dpkg -i /tmp/gum.deb && rm /tmp/gum.deb
fi

# ── Helpers ───────────────────────────────────────────────────────
header()  { gum style --border thick --padding "1 2" --margin "0 0 1 0" "$1"; }
ok()      { gum style --foreground 42 "  ✓ $1"; }
warn()    { gum style --foreground 220 "  ! $1"; }

# ── Theme list ─────────────────────────────────────────────────────
declare -A THEMES
THEMES["catppuccin mocha"]="catppuccin-mocha.sh"
THEMES["catppuccin macchiato"]="catppuccin-macchiato.sh"
THEMES["gruvbox dark"]="gruvbox-dark.sh"
THEMES["gruvbox light"]="gruvbox-light.sh"
THEMES["solarized dark"]="solarized-dark.sh"
THEMES["solarized light"]="solarized-light.sh"

THEME_ORDER=(
  "catppuccin mocha"
  "catppuccin macchiato"
  "gruvbox dark"
  "gruvbox light"
  "solarized dark"
  "solarized light"
)

# ── Resolve theme ─────────────────────────────────────────────────
resolve_theme() {
  local theme="$1"
  local variant="${2:-}"
  local key

  if [[ -n "$variant" ]]; then
    key="$theme $variant"
    [[ -n "${THEMES[$key]:-}" ]] && { echo "$key"; return; }
  fi

  # Default variant per family
  case "$theme" in
    catppuccin) echo "catppuccin mocha"; return ;;
    gruvbox)    echo "gruvbox dark";    return ;;
    solarized)  echo "solarized dark";  return ;;
  esac

  echo ""
}

# ── Source palette ────────────────────────────────────────────────
load_palette() {
  local key="$1"
  local file="${THEMES[$key]}"
  source "$THEMES_DIR/$file"
}

# ── Write generated files ──────────────────────────────────────────

wc() {
  local f="$1"
  if $DRY_RUN; then
    gum style --foreground 33 "  ~ would write $f"
    if [[ -t 0 ]]; then return; fi
    local line
    while IFS= read -r line; do
      gum style --foreground 33 "    $line"
    done
    return
  fi
  mkdir -p "$(dirname "$f")"
  cat > "$f"
}

write_hypr_colors() {
  local f="$HOME/.config/hypr/colors.conf"
  wc "$f" <<EOF
# Hyprland colors — generated by theme-switch.sh
\$border_active   = rgba($(strip_hash $TH_BORDER_ACTIVE)ee)
\$border_inactive = rgba($(strip_hash $TH_BORDER_INACTIVE)ee)
EOF
  $DRY_RUN || ok "hypr colors → $f"
}

write_waybar_colors() {
  local dir="$HOME/.config/waybar"
  wc "$dir/colors.css" <<EOF
/* waybar colors — generated by theme-switch.sh */
@define-color bg      #$(strip_hash $TH_BG);
@define-color fg      #$(strip_hash $TH_FG);
@define-color accent  #$(strip_hash $TH_ACCENT);
@define-color surface #$(strip_hash $TH_SURFACE);
@define-color overlay #$(strip_hash $TH_OVERLAY);
@define-color green   #$(strip_hash $TH_GREEN);
@define-color red     #$(strip_hash $TH_RED);
@define-color yellow  #$(strip_hash $TH_YELLOW);
@define-color blue    #$(strip_hash $TH_BLUE);
@define-color magenta #$(strip_hash $TH_MAGENTA);
@define-color cyan    #$(strip_hash $TH_CYAN);
EOF
  $DRY_RUN || ok "waybar colors → $dir/colors.css"
  # Create base style.css if it doesn't exist
  if [[ ! -f "$dir/style.css" ]]; then
    wc "$dir/style.css" <<'CSS'
@import "colors.css";
* { font-family: "JetBrains Mono Nerd Font", sans-serif; font-size: 13px; }
window#waybar { background: @bg; color: @fg; }
#workspaces button { background: @surface; color: @fg; border-radius: 4px; margin: 2px; }
#workspaces button.active { background: @accent; color: @bg; }
#clock, #network, #bluetooth, #pulseaudio, #battery, #tray { padding: 0 8px; }
#clock { color: @accent; }
#network { color: @green; }
#pulseaudio { color: @cyan; }
#battery { color: @green; }
#battery.critical { color: @red; }
CSS
  fi
}

